/*
  Method : public string replace(char oldChar, Char newChar);

  Description : replaces every instance of character in the given string with a new character

  Paramter : character (old char), character (new char)

  Return Type : String

*/

class ReplaceString {

	public static void main(String[] args) {

		String str1 = "MJNISH";

		System.out.println(str1.replace('J','A'));
	}
}
