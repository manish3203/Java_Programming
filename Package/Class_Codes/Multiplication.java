package arithoperator;

//Multiplication Class
public class Multiplication {

        int num1 = 0;
        int num2 = 0;

        public Multiplication(int num1, int num2) {

                this.num1 = num1;
                this.num2 = num2;
        }
        public int mul() {

                return num1*num2;
        }
}

