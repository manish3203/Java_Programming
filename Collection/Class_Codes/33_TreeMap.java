
import java.util.*;

class TreeMapDemo {

	public static void main(String[] args) {

		TreeMap tm = new TreeMap();
		
		tm.put("Ind","India");
		tm.put("Pak","Pakistan");
		tm.put("SL","SriLanka");
		tm.put("Ban","Bangladesh");
		tm.put("Aus","Australia");
		
		System.out.println(tm);
	}
}

