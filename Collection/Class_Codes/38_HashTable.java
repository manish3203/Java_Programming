
import java.util.*;

class HashTableDemo {

	public static void main(String[] args) {

		Hashtable ht = new Hashtable();

		ht.put(10,"Sachine");
		ht.put(7,"Infosys");
		ht.put(18,"virat");
		ht.put(45,"Rohit");
		ht.put(1,"Rahul");

		System.out.println(ht);
	}
}

/*
 
{10=Sachine, 18=virat, 7=Infosys, 1=Rahul, 45=Rohit}

*/
